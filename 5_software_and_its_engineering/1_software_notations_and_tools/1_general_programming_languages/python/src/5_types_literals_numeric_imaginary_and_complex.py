a_complex_number = 5_900+9j
print(a_complex_number)

a_complex_number = 5+9J
print(a_complex_number)

a_complex_number = 5+9.98j
print(a_complex_number)

a_complex_number = 5.90+9.98j
print(a_complex_number)
